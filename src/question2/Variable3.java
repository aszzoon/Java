public class Variable3 {
    public static void main(String[] args) {
        int x = 3;
        int y = 4;
        int z = 5;
        int sum;

        sum = x+y+z;

        System.out.println("합계 = "+(x+y+z));
        System.out.println("평균 = "+(x+y+z)/3);
        System.out.println("평균 = "+sum/3);

        //3개의 int형 변수에 각각 값을 대입하여 합계와 평균을 구하 프로그램을 작성하자
    }
}
