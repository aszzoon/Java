package question2;

public class Variable {
    public static void main(String[] args) {
        System.out.println(82+17);
        System.out.println(82-17);

        System.out.println("82+17 ="+(82+17));
        System.out.println("82-17 ="+(82-17));

        // 정수 82와 17의 합과 차를 구하는 프로그램 다양한 방법으로 작성 해보자
    }
}
